#include <unistd.h>

int	ft_putstr(char *str)
{
	int	c;
	int	i;

	i = 0;
	if (!str)
		return (write(1, "(null)", 6));
	while (str[i] != '\0')
	{
		c = write(1, &str[i], 1);
		if (c == -1)
			return (c);
		i++;
	}
	return (i);
}
